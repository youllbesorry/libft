#include "libft.h"

int	ft_tolower(int c)
{
	char	d;

	d = c;
	if (c >= 'A' && c <= 'Z')
		d = c + 32;
	else
		return (c);
	if (!(d >= 'a' && d <= 'z'))
		return (c);
	return (d);
}
