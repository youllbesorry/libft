#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(nb *= -1);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_itoa(nb);
}

char	*ft_itoa(int n)
{
	char	*str;

	n += '0';
	str = malloc(sizeof(char) * ft_strlen(n));
}
